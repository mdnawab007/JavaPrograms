class EVENODD_5
{
    public static void main()
    {
        evod(10);
        evod(21);
    }

    public static void evod(int a)
    {
        if(a%2==0)
        {
            System.out.println("NUMBER IS EVEN");
        }
        else
        {
            System.out.println("NUMBER IS ODD");
        }
    }
}