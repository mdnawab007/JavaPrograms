class PATTERN_7
{
    public static void main()
    {
        
        for (int i =9; i >1; i--)
        {
            for (int j = i; j >= 1; j--)
            {
                System.out.print("*");
            }
            System.out.println();
        }

        for (int i = 1; i <9; i++)
        {
            for (int j = i; j >=1; j--)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}






