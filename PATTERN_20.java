class PATTERN_20
{
    public static void main()
    { 
        int a=1;
        for(int i=1;i<=5;i++)
        {
            for(int k=1;k<=a;k++)
            {
                System.out.print(" ");
            }
            a++;
            for(int j=1;j<=5;j++)
            {
                System.out.print("X");
            }
            
            System.out.println();

        }
    }
}

